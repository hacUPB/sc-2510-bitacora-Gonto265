¿Que es una computadora digital moderna?

- Es un dispositiva electrónico que procesa información, utiliza códigos binarios, almacena bastante información, puede realizar cálculos aritméticos, permite escribir textos, navegación por el internet, etc.

¿Cúales son sus partes?

- Está compuesta por una fuente de alimentación, La placa madre, Memoria RAM, Unidad de disco duro, microprocesador, gabinete y sus respectivos conectores, cables y puertos.

